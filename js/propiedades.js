const { createApp } = Vue

  createApp({
    data() {
      return {
        url:"https://juanvi.pythonanywhere.com/propiedades",
        productos:[],
        error:false,
        cargando:true
      }
    },
    // Se llama despuÃ©s de que la instancia haya 
    // terminado de procesar todas las opciones relacionadas con el estado.
    created() {
        this.fetchData(this.url)
    },
    methods: {
        fetchData(url) {
            fetch(url)
                .then(response => response.json())
                .then(data => {
                    this.propiedades = data;
                    this.cargando=false
                })
                .catch(err => {
                    console.error(err);
                    this.error=true              
                })
        },
        eliminar(propiedad) {
            const url = 'https://juanvi.pythonanywhere.com/propiedades/' + propiedad; /*producto es el id del produto*/
            var options = {
                method: 'DELETE',
            }
            fetch(url, options)
                .then(res => res.text()) // or res.json()
                .then(res => {
                    location.reload();
                })
        }
    },
  }).mount('#app')